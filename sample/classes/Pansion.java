package sample.classes;

public class Pansion {
    private boolean breakfast;
    private boolean lunch;
    private boolean snack;
    private boolean supper;

    private boolean hotelDay;
    private boolean hotelAround;

    public boolean isBreakfast() {
        return breakfast;
    }

    public void setBreakfast(boolean breakfast) {
        this.breakfast = breakfast;
    }

    public boolean isLunch() {
        return lunch;
    }

    public void setLunch(boolean lunch) {
        this.lunch = lunch;
    }

    public boolean isSnack() {
        return snack;
    }

    public void setSnack(boolean snack) {
        this.snack = snack;
    }

    public boolean isSupper() {
        return supper;
    }

    public void setSupper(boolean supper) {
        this.supper = supper;
    }

    public boolean isHotelDay() {
        return hotelDay;
    }

    public void setHotelDay(boolean hotelDay) {
        this.hotelDay = hotelDay;
    }

    public boolean isHotelAround() {
        return hotelAround;
    }

    public void setHotelAround(boolean hotelAround) {
        this.hotelAround = hotelAround;
    }
}
